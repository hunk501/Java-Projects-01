package com.lira.filesharing;

public class Host {

	private static final String host = "";
	private static final int port = 5555;
	
	/**
	 * Get the Host
	 * @return
	 */
	public static String getHost(){
		return host;
	}
	
	/**
	 * Get the Port
	 * @return
	 */
	public static int getPort(){
		return port;
	}
}
